    public TerrainType getCase(int i, int j) {
        switch (terrain[i][j]) {
            case 0:
                return TerrainType.GRASS;
            case 1:
                return TerrainType.PATH;
            case 2:
                return TerrainType.BLOCKED;
            case 3:
                return TerrainType.BATS;
            case 4:
                return TerrainType.BLOCKAGE;
            case 5:
                return TerrainType.TOWER_BASE;
            default:
                return null;
        }
    }

