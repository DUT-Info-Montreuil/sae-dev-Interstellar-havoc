    public static Terrain getInstance(){
        if(uniqueInstance==null){
            uniqueInstance= new Terrain();
        }
        return uniqueInstance;
    }

    public TerrainType getCase(int i, int j) {
        switch (terrain[i][j]) {
            case 0:
                return TerrainType.terrain;
            case 1:
                return TerrainType.chemin;
            case 2:
                return TerrainType.mur;
            case 3:
                return TerrainType.base_tourelle;
            case 4:
                return TerrainType.cheminBloque;
            case 5:
                return TerrainType.base_finale;
            default:
                return null;
        }
    }

    public void setCheminNull(LinkedList<Cellule> cheminPlusCourt) {
        this.cheminPlusCourt = null;
    }

    public int[] getPosDansCarte(int x, int y) {
        // on recupere l'emplacement par rapport au terrain
        return new int[]{y / 16, x / 16};
    }

    public int getCase1(int i, int j) {
        return terrain[i][j];
    }

    public void placementObjetMur(int i, int j) {
        if (terrain[i][j] == 1) {
            terrain[i][j] = 2;
        } else if (terrain[i][j] == 2) {
            terrain[i][j] = 1;
        }
    }

    public void placementMur(int i, int j) {
        if (terrain[i][j] == 2) {
            terrain[i][j] = 1;
        } else if (terrain[i][j] == 1) {
            terrain[i][j] = 2;
        }
    }

    public LinkedList<Cellule> recalculerCheminPourEnnemi(Ennemi ennemi, int[] positionActuelle) {
        BFS bfsUtilisé = ennemi.peutContournerMur() ? bfsContournerMur : bfsDirect;
        bfsUtilisé.plusCourtChemin2(terrain, positionActuelle, fin);
        return bfsUtilisé.getPlusCourtChemin();
    }

    /* les getter et setter */
    public LinkedList<Cellule> getCheminPourEnnemi(boolean peutContournerMur) {
        return peutContournerMur ? bfsContournerMur.getPlusCourtChemin() : bfsDirect.getPlusCourtChemin();
    }


    public int[][] getTerrain() {
        return terrain;
    }
